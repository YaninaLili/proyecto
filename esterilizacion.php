        No es cierto que el animal necesite tener una camada para completar su ciclo vital, puesto que no existe una relación consciente con la reproducción como parte necesaria de su bienestar emocional.

Cuando decidimos reproducir a un perro o a un gato tenemos que pensar que no solamente nacerán 1 ó 2 cachorros. Nacerán en muchas ocasiones mas de 6 ó 7 (en ocasiones 12 ó más) y estos animales merecen por igual una casa en la que tengan seguridad, cuidados, cariño y, lo mas importante de todo, permanencia. De cada 10 cachorros de raza, únicamente 2 permanecerán al año de vida en su casa original. Esto resulta en el abandono de los otros 8 cachorros, quienes terminarán su vida en la calle, en refugios o en un antirrábico. 
Es importante tomar en cuenta que al cruzar a tu perro o gato no obtendrás una copia del mismo, será un individuo con caracterí­sticas fí­sicas y temperamentales propias.

La mayorí­a de las personas que cruzan a sus perras o gatas tienen buenas intenciones para los cachorros y expectativas sinceras de acomodarlos pero, desafortunadamente, es muy difí­cil poder colocar bien a TODOS los cachorros, y esto casi nunca suele ocurrir.

